#ifndef CHIP8_HPP
#define CHIP8_HPP

#include <cstdint>
#include <string>

#include <SFML/Graphics.hpp>

class Chip8
{
public:
  bool loadRom(std::string fileName);
  void loadWindow();
  void handleInput(sf::RenderWindow &window);
  int mapSFMLKeyToChip8(sf::Keyboard::Key key);
  void cycle(sf::RenderWindow &window);

  Chip8()
  {
    // Set up font in first 0x50 (80) in memory
    for (uint8_t i = 0; i < 0x50; ++i)
    {
      memory[i] = fontset[i];
    }
  }

  uint8_t display[64 * 32] = {0};
  uint8_t keypad[16] = {0};

private:
  uint8_t memory[0x1000] = {0};
  uint8_t registers[16] = {0};
  uint16_t indexRegister = 0;
  uint16_t programCounter = 0x200;

  uint16_t stack[16] = {0};
  uint8_t stackPointer = 0;

  uint8_t delayTimer = 0;
  uint8_t soundTimer = 0;

  bool drawFlag = false;

  const uint8_t fontset[80] = {
      0xF0, 0x90, 0x90, 0x90, 0xF0,
      0x20, 0x60, 0x20, 0x20, 0x70,
      0xF0, 0x10, 0xF0, 0x80, 0xF0,
      0xF0, 0x10, 0xF0, 0x10, 0xF0,
      0x90, 0x90, 0xF0, 0x10, 0x10,
      0xF0, 0x80, 0xF0, 0x10, 0xF0,
      0xF0, 0x80, 0xF0, 0x90, 0xF0,
      0xF0, 0x10, 0x20, 0x40, 0x40,
      0xF0, 0x90, 0xF0, 0x90, 0xF0,
      0xF0, 0x90, 0xF0, 0x10, 0xF0,
      0xF0, 0x90, 0xF0, 0x90, 0x90,
      0xE0, 0x90, 0xE0, 0x90, 0xE0,
      0xF0, 0x80, 0x80, 0x80, 0xF0,
      0xE0, 0x90, 0x90, 0x90, 0xE0,
      0xF0, 0x80, 0xF0, 0x80, 0xF0,
      0xF0, 0x80, 0xF0, 0x80, 0x80};
};

#endif